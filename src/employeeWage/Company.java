package employeeWage;

public class Company {
    int companyWage;
    int companyWorkingHours;
    int companyWorkingDays;

    public int getCompanyWage() {
        return companyWage;
    }

    public void setCompanyWage(int companyWage) {
        this.companyWage = companyWage;
    }

    public int getCompanyWorkingHours() {
        return companyWorkingHours;
    }

    public void setCompanyWorkingHours(int companyWorkingHours) {
        this.companyWorkingHours = companyWorkingHours;
    }

    public int getCompanyWorkingDays() {
        return companyWorkingDays;
    }

    public void setCompanyWorkingDays(int companyWorkingDays) {
        this.companyWorkingDays = companyWorkingDays;
    }
}
